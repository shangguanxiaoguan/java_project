package objectorientedstudy.extenddemo;

public class ParentDemo_4 {
    private String name;
    private int age;

    public ParentDemo_4(String name, int age) {
        this.name = name;
        this.age = age;
    }

    public ParentDemo_4(){
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }
}
